# Ejercicio 2: Procesar lista de números

numeros = [5, 2, 8, 1, 9, 3]

suma = sum(numeros)
promedio = suma / len(numeros)
maximo = max(numeros)
minimo = min(numeros)

print(f"Lista: {numeros}")
print(f"Suma: {suma}")
print(f"Promedio: {promedio}")
print(f"Máximo: {maximo}")
print(f"Mínimo: {minimo}")